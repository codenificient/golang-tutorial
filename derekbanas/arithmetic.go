package main

import "fmt"

func main() {
	var numOne = 1.000
	var num99 = 0.999

	fmt.Println(numOne - num99)

	// more arithmetic operations

	fmt.Println("6 + 4 = ", 6+4)
	fmt.Println("6 - 4 = ", 6-4)
	fmt.Println("6 * 4 = ", 6*4)
	fmt.Println("6 / 4 = ", 6/4)
	fmt.Println("6 % 4 = ", 6%4)
}
